void PrintImage(int[,] image)
{
    for(int rows = 0; rows < image.GetLength(0); rows++)
    {
        for(int col = 0; col < image.GetLength(1); col++)
        {
            // Console.Write($" {image[rows, col]} ");
            if(image[rows, col] == 0) Console.Write($" ");
            else Console.Write($"*");
        }
        Console.WriteLine();
    }
}

void Fillimage(int row, int col)
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 1;
        Fillimage(row - 1, col);
        Fillimage(row, col - 1);
        Fillimage(row + 1, col);
        Fillimage(row, col + 1);
    }
}


